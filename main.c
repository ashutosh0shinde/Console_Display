void delay(int ms)
{
    #ifdef _WIN32
    Sleep(ms); // for windows: ms
    #else
    usleep(ms*1000); // for linux/macos: uf*1000 = ms
    #endif
}
void cls(){
    // #ifdef _WIN32
    // system("cls");
    // #else
    // system("clear");
    // #endif
    printf("\033[2J\033[H"); //moves cursor to top left, creating 
                            // illusion of refrashing screen
}
void resetScreen()
{
    int i;
    char *ptr = *screen;
    for(i = 0;i<ROW*COL;i++)
    {
        *(ptr+i) = offchar; 
    }
}
void drawPixel(int x, int y)
{
    screen[x][y] = onchar;
}
void render()
{
    int i,j;
    printf("\n");
    for(i =0; i<ROW; i++)
    {
        for(j = 0;j<COL; j++)
        {
            if(i == 0 || i == ROW-1 || j == 0 || j == COL-1)
            {
                printf("%c", BORDER);
            }
            else
            printf("%c",screen[i][j]);
        }
        printf("\n");
    }
    printf("\n");
}
