//mode 'l' = left scrolling
// 'r' = right scrolling
// 's' static
void drawString(char st[13], int x, int y) 
{
    int i = 0;
    while(st[i] != '\0')
    {
        st[i] = toupper(st[i]);
        int ind = st[i] - 'A';
        printChar(ind,x,y+(i*C_Y));
        i++;
    }
}
