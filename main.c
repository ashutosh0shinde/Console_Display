void drawPixel(int x, int y);
void printChar(int ind, int st_x, int st_y);

//mode 'l' = left scrolling
// 'r' = right scrolling
// 's' static
void drawString(char st[13], int x, int y) 
{
    int i = 0;
    while(st[i] != '\0')
    {
        st[i] = toupper(st[i]);
        int ind = st[i] - 'A';
        printChar(ind,x,y+(i*C_Y));
        i++;
    }
}
void printChar(int ind, int st_x,int st_y)
{
    int i,j;
    for(i = 0; i < C_X; i++)
    {
        for(j = 0; j < C_Y; j++)
        {
            if(letters[ind][i][j] == onchar)
            {
                drawPixel((i+st_x)%ROW, (j+st_y)%COL); //may give negative index if negative movement is hude
            }
        }
    }
}
void delay(int ms)
{
    #ifdef _WIN32
    Sleep(ms); 
    #else
    usleep(ms*1000); 
    #endif
}
void cls()
{
    printf("\033[2J\033[H");
}
void resetScreen()
{
    int i;
    char *ptr = *screen;
    for(i = 0;i<ROW*COL;i++)
    {
        *(ptr+i) = offchar; 
    }
}
void drawPixel(int x, int y)
{
    screen[x][y] = onchar;
}
void render()
{
    int i,j;
    printf("\n");
    for(i =0; i<ROW; i++)
    {
        for(j = 0;j<COL; j++)
        {
            if(i == 0 || i == ROW-1 || j == 0 || j == COL-1)
            {
                printf("%c", BORDER);
            }
            else
            printf("%c",screen[i][j]);
        }
        printf("\n");
    }
    printf("\n");
}
void Display(char st[13], int x, int y, char dir, int speed)
{
    speed = 10 - speed;
    speed = speed < 1 ? 1 : speed;
    
    int i;
    if(dir == 'l')
    {
        for(i = 100; i > 0; i--)
        {
            cls();
            resetScreen();
            drawString(st,2,i);
            render();    
            delay(5 * speed);
        }
    }
    else if (dir == 'r')
    {
        for(i = 0; i < 100; i++)
        {
            cls();
            resetScreen();
            drawString(st,2,i);
            render();    
            delay(5 * speed);
        }
    }
    else
    {
        cls();
        resetScreen();
        drawString(st,2,2);
        render();    
        delay(5 * speed);
    }
}
int main()
{
    resetScreen();
    
    char ch[20];
    char dir;
    int speed;
    do
    {
        printf("Enter string (letters only) (max length 13): ");
        fgets(ch, sizeof(ch), stdin);
        ch[strcspn(ch,"\n")] = '\0';
    }while(strlen(ch) > 13 || strlen(ch) == 0);

    printf("Enter scrolling direction (l=left, r=right, n=no scroll): ");
    scanf(" %c",&dir);

    if(dir != 'l' && dir != 'r')
    {
        Display(ch, 2, 0, dir , speed);
        return 0;
    }

    printf("Enter the speed of scrolling (1 - 10): ");
    scanf("%d",&speed);

    Display(ch, 2, 0, dir , speed);
    return 0;
}
